#include <stdio.h>
#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != '\0' && s[i] != (char)c)
		i ++;
	if (s[i] == (char)c || !c)
		return ((char *)s + i);
	return (NULL);
}
